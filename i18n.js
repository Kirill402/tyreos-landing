  // ── Detect locale ──────────────────────────────────────────────────────────
  function detectLocale() {
    var langs = (navigator.languages && navigator.languages.length)
      ? navigator.languages
      : [navigator.language || 'ru'];
    for (var i = 0; i < langs.length; i++) {
      var base = langs[i].split('-')[0].toLowerCase();
      if (base === 'ru') return 'ru';
      if (T[base])       return base;
    }
    return 'en'; // international fallback
  }

  var locale = detectLocale();
  document.documentElement.lang = locale;

  function t(key) {
    return (T[locale] && T[locale][key] != null) ? T[locale][key]
         : (T['ru'][key] != null)                ? T['ru'][key]
         : key;
  }

  // Expose globally for chat.js and any future scripts
  window.__i18n = { locale: locale, t: t };

  // ── Apply translations ─────────────────────────────────────────────────────
  function apply() {
    var i, el, val;

    // textContent
    var plain = document.querySelectorAll('[data-i18n]');
    for (i = 0; i < plain.length; i++) {
      val = t(plain[i].getAttribute('data-i18n'));
      if (val) plain[i].textContent = val;
    }

    // innerHTML  (for <br> in headings etc.)
    var rich = document.querySelectorAll('[data-i18n-html]');
    for (i = 0; i < rich.length; i++) {
      val = t(rich[i].getAttribute('data-i18n-html'));
      if (val) rich[i].innerHTML = val;
    }

    // placeholder
    var ph = document.querySelectorAll('[data-i18n-placeholder]');
    for (i = 0; i < ph.length; i++) {
      val = t(ph[i].getAttribute('data-i18n-placeholder'));
      if (val) ph[i].placeholder = val;
    }

    // <title> and <meta description>
    document.title = t('meta.title');
    el = document.querySelector('meta[name="description"]');
    if (el) el.setAttribute('content', t('meta.desc'));
  }

  // Script is placed at end of <body> — DOM is already available.
  // Guard against edge-cases (e.g. deferred loading) with readyState check.
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', apply);
  } else {
    apply();
  }
